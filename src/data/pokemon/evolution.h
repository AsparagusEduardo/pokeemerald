#define NO_EVOLUTION(name)                              \
static const struct Evolution s##name##Evolutions[] = { \
    EVOLUTIONS_END,                                     \
};

#define SINGLE_EVOLUTION(name, method, param, species)  \
static const struct Evolution s##name##Evolutions[] = { \
    {method, param, species},                           \
    EVOLUTIONS_END,                                     \
};

#define DOUBLE_EVOLUTION(name, method1, param1, species1, method2, param2, species2)  \
static const struct Evolution s##name##Evolutions[] = { \
    {method1, param1, species1},                        \
    {method2, param2, species2},                        \
    EVOLUTIONS_END,                                     \
};

